package org.cinematics.model;

public class Customer {
	private int custId;
	private String name;
	
	
	public void setCustId(int id) {
		custId = id;
	}
	public int getCustId() {
		return custId;
	}
	public void setName(String n) {
		name = n;
	}
	public String getName() {
		return name;
	}
}
